import lombok.Getter;
import lombok.Setter;

@Getter
@Setter
public class OwnerRegisterRequestDto {

    @NotBlank
    private String fullName;

    @Email
    @NotBlank
    private String email;

    @NotBlank
    private String phone;

    @NotBlank
    private String password;
}
